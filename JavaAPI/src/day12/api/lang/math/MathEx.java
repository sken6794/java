package day12.api.lang.math;

public class MathEx {
	public static void main(String[] args) {
		
		//난수 생성 random
		double d = Math.random();		
		System.out.println(d*10+1);
		
		//절대값 abs 
		System.out.println(Math.abs(-3.14));
		
		//올림 ceil 
		System.out.println(Math.ceil(3.15));
		
		//내림 floor
		System.out.println(Math.floor(3.15));
		
		//반올림 round
		System.out.println(Math.round(3.55));
		
		//작은수 min , 큰 수 max
		System.out.println(Math.max(5, 8.8));
		
		
		
		
	}
}









